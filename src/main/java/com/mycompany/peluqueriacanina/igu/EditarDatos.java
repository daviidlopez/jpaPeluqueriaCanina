    private void cargarDatos() {
        txtCelDuenio.setText(_mascota.getDuenio().getCelular());
        txtColor.setText(_mascota.getColor());
        txtNomDuenio.setText(_mascota.getDuenio().getNombre());
        txtNombre.setText(_mascota.getNombre());
        txtObservaciones.setText(_mascota.getObservaciones());
        txtRaza.setText(_mascota.getRaza());
        
        if (_mascota.getAlergico().equals("SI"))
            cmbAlergico.setSelectedIndex(1);
        else
            cmbAlergico.setSelectedIndex(2);
        
        if (_mascota.getAtencion_especial().equals("SI"))
            cmbAtEsp.setSelectedIndex(1);
        else
            cmbAtEsp.setSelectedIndex(2);
    }
