    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        _mascota.setNombre(txtNombre.getText());
        _mascota.setRaza(txtRaza.getText());
        _mascota.setColor(txtColor.getText());
        _mascota.setObservaciones(txtObservaciones.getText());
        
        _duenio.setNombre(txtNomDuenio.getText());
        _duenio.setCelular(txtCelDuenio.getText());
        _mascota.setDuenio(_duenio);
        
        _mascota.setAlergico((String) cmbAlergico.getSelectedItem());
        _mascota.setAtencion_especial((String) cmbAtEsp.getSelectedItem());
        
        
        controladora.editarMascota(_mascota);
        JOptionPane optionPane = new JOptionPane("Se editó correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Editado exitoso");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }//GEN-LAST:event_btnGuardarActionPerformed

